/**
 * Конфигурация веб-визуализации событийной системы балансировки
 * 
 * @description События это порция информации, произошедшая в какое-то время
 * Веб-интерфейс адаптирует поток событий для человеческого восприятия
 */
export interface IWebDashboardConfig {
    /** Интервал обновления UI в миллисекундах (реактивность системы) */
    updateIntervalMs?: number

    /** Максимальное количество отображаемых элементов в очереди */
    maxDisplayItems?: number

    /** Включить анимации для визуализации потоков событий */
    animationEnabled?: boolean

    /** Автоматически запускать систему при загрузке */
    autoStart?: boolean
} 